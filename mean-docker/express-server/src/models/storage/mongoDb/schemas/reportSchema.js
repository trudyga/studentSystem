const mongoose = require('mongoose');
const Schema = mongoose.Schema;

let reportSchema = new Schema({
    title: {
        type: String,
        required: true
    },
    type: {
        type: String,
        required: true
    },
    content: {
        type: String,
        required: true
    },
    author: {
        type: String,
        required: true
    },
    createdAt: {
        type: Date,
        required: true
    },
    updatedAt: {
        type: Date,
        required: true
    }
});

mongoose.model('Report', reportSchema);
module.exports = reportSchema;